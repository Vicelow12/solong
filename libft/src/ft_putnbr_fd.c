#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	digit;

	if (n < 0)
	{
		if (n == -2147483648)
		{
			n = 147483648;
			write(fd, "-2", 2);
		}
		else
		{
			write(fd, "-", 1);
			n = -n;
		}
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	digit = '0' + n % 10;
	write(fd, &digit, 1);
}
