#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*cpy;

	i = 0;
	cpy = (char *)s;
	while (i < n)
	{
		cpy[i] = c;
		i++;
	}
	return (s);
}
